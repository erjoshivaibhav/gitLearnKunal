package JavaBasics;

public class datatypes {

	public static void main(String[] args) {

		String a = "I love you Vaibhav Vaibhav";
	    String b="I love you Sonali";

		System.out.println(a.replace("Vaibhav", "Sonali"));
		System.out.println(a.replace("a", "b"));
		
		System.out.println(a.replaceAll("Vaibhav", "Sonali"));
		System.out.println(a.replaceAll("", "S"));
		System.out.println(a.replace(a, b));
		
		
		
		
		

	}

}
